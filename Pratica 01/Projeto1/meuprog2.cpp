#include <iostream>
using namespace std;

int main() {
    int A, B;
    cout << "Digite um numero inteiro: ";
    cin >> A;
    cout << "Digite outro numero inteiro: ";
    cin >> B;
    cout << "Soma: " << A+B << endl;
    cout << "Subtração: " << A-B << endl;
    cout << "Multiplicação: " << A*B << endl;
    cout << "Divisão: " << A/B << endl;
    cout << "Resto: " << A%B << endl;
    return 0;
}
